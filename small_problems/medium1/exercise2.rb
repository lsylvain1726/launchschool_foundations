# takes two numbers, one is the number
# second is the value, n times, it should be rotated
# rotate the last n digits of a number

def rotate_array(array)
array[1..-1] + [array[0]]
end

def rotate_rightmost_digits(num, count)
array = num.to_s.split('')
last_digits = array.pop(count)
array << rotate_array(last_digits)
array.flatten.join('').to_i
end

p rotate_rightmost_digits(735291, 1) == 735291
p rotate_rightmost_digits(735291, 2) == 735219
p rotate_rightmost_digits(735291, 3) == 735912
p rotate_rightmost_digits(735291, 4) == 732915
p rotate_rightmost_digits(735291, 5) == 752913
p rotate_rightmost_digits(735291, 6) == 352917

def rotate_rightmost_digits(number, n)
  all_digits = number.to_s.chars
  all_digits[-n..-1] = rotate_array(all_digits[-n..-1])
  all_digits.join.to_i
end

